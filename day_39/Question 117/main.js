// Question 117: Implement a switch statement that evaluates an expression and uses the default case if none of the cases match.
let x = 1;
switch (x) {
    case 1:
        console.log("x is 1");
        break;
    case 2:
        console.log("x is 2");
        break;
    case 3:
        console.log("x is 3");
        break;
    default:
        console.log("x is not 1, 2, or 3");
        break;
}
export {};
